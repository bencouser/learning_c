#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{

        int num = 5;
        printf("%d \n", num);
        printf("%d \n", 5 / 4);
        printf("%f \n", 5 + 4.0);

        printf("%f \n", pow(2, 3));
        printf("%f \n", sqrt(4));
        printf("%f \n", ceil(77.7));
        printf("%f \n", floor(36.574));

        return 0;

}
